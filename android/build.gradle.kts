allprojects {
    repositories {
        // Primary: Google and MavenCentral (most reliable)
        google()
        mavenCentral()
        // Fallback: Aliyun mirrors (only if primary fails)
        maven { url = uri("https://maven.aliyun.com/repository/google") }
        maven { url = uri("https://maven.aliyun.com/repository/public") }
    }
}

